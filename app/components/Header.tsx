function activeLinkStyle({
  isActive,
  isPending,
}: {
  isActive: boolean;
  isPending: boolean;
}) {
  return {
    fontWeight: isActive ? '600' : undefined,
    color: isPending ? '#a8a29e' : isActive ? '#15803d' : undefined,
  };
}
